void	ft_bzero(void *s, int tamanho)
{
	int		i;
	char	*frase;

	frase = s;
	i = 0;
	if (tamanho > 0)
	{
		while (i < tamanho)
		{
			if (i < tamanho)
				frase[i] = '\0';
			i++;
		}
	}
}
